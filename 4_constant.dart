void main() {
  // Final -> can only be set once.
  final firstName = "Brian";
  final String lastName = "Yuko";
  print("$firstName $lastName");

  // Const -> Constant variables can't be assigned a value.
  const number = 999;
  const double multiplyNumber = number * 2.5;
  print(number);
  print(multiplyNumber);
}
